#include "libft.h"

int	ft_isxdigit(int c)
{
	unsigned char g;

	g = (unsigned char)c;
	if ((g >= '0' && g <= '9') ||
		(g >= 'A' && g <= 'F') ||
		(g >= 'a' && g <= 'f'))
		return (1);
	return (0);
}
